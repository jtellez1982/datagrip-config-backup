SELECT C.C7                                         AS Documento_SAP
     , C.C9                                         AS Folio
     , FORMAT(C.C3, 'dd/MM/yyyy HH:mm')             AS Fecha_de_Factura
     , C.C4                                         AS Customer_SAP_ID_Pag
     , C.C5                                         AS Customer_SAP_ID_Sol
     , C.C6                                         AS Nombre_del_Cliente
     , C.C8                                         AS Correlative_Number
     , C.C10                                        AS Importe_Neto
     , C.C13                                        AS Numero_de_Caja
     , C.C12                                        AS Folio_Acuse
     , P.PAGINAS
     , FORMAT(D.FECHA_CREACION, 'dd/MM/yyyy HH:mm') AS FECHA_CREACION
     , C.C14                                        AS Etiqueta
    FROM G_DOCUMENTOS D
             INNER JOIN G_CAMPOS_01C0000003 C ON C.GID = D.GID
             INNER JOIN (SELECT GID_DOCUMENTO, SUM(IMAGENES) AS PAGINAS
                             FROM G_PAGINAS
                             GROUP BY GID_DOCUMENTO) P ON D.GID = P.GID_DOCUMENTO
    WHERE CONVERT(DATE, FECHA_CREACION, 103) BETWEEN '2024-09-21' AND '2024-10-20'

